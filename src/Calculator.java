public class Calculator {
    public void add(int a, int b) {
        int result = a + b;
        System.out.println(result);
    }
    public void subtraction(int a, int b) {
        int result = a - b;
        System.out.println(result);
    }

    public void multiplication(int a, int b) {
        int result = a * b;
        System.out.println(result);
    }

    public void division(int a, int b) {
        int result = a / b;
        System.out.println(result);
    }

}
